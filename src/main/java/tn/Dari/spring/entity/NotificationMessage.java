package tn.Dari.spring.entity;

public class NotificationMessage {


	  private String name;

	 

	  public NotificationMessage() {
		super();
	}

	

	  public NotificationMessage(String name) {
		super();
		this.name = name;
	}



	public String getName() {
	    return name;
	  }

	  public void setName(String name) {
	    this.name = name;
	  }
	

}
